
public class Looping {

	public static void main(String[] args) {
		//While Looping
		int x=0;
		while(x<5) {
			System.out.println(x);
			x++;
		}
		
		//For looping
		for(int i=0;i<=2;i++) {
			System.out.println("Hello My Friend!");
		}
		
		//Do While looping
		int y=0;
		do {
			System.out.println("Hello World!");
			y++;
		}while(y<3);
	}

}
